	}

	/**
	 * Build a Criteria object containing the values of all modified columns in this object.
	 *
	 * @return     Criteria The Criteria object containing all modified values.
	 */
	public function buildCriteria()
	{
		$criteria = new Criteria(SessionsPeer::DATABASE_NAME);

		if ($this->isColumnModified(SessionsPeer::ID)) $criteria->add(SessionsPeer::ID, $this->id);
		if ($this->isColumnModified(SessionsPeer::USER_ID)) $criteria->add(SessionsPeer::USER_ID, $this->user_id);
		if ($this->isColumnModified(SessionsPeer::IP_ADDRESS)) $criteria->add(SessionsPeer::IP_ADDRESS, $this->ip_address);
		if ($this->isColumnModified(SessionsPeer::USER_AGENT)) $criteria->add(SessionsPeer::USER_AGENT, $this->user_agent);
		if ($this->isColumnModified(SessionsPeer::PAYLOAD)) $criteria->add(SessionsPeer::PAYLOAD, $this->payload);
		if ($this->isColumnModified(SessionsPeer::LAST_ACTIVITY)) $criteria->add(SessionsPeer::LAST_ACTIVITY, $this->last_activity);

		return $criteria;
	}

