	public function copyInto($copyObj, $deepCopy = false)
	{

		$copyObj->setUserId($this->user_id);

		$copyObj->setIpAddress($this->ip_address);

		$copyObj->setUserAgent($this->user_agent);

		$copyObj->setPayload($this->payload);

